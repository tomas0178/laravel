    {
        $request->validate([
            'description' => 'required', 
        ]);

        Tweet::create([
            'description' => $request->input('description'),
            'user_id' => auth()->user()->id
        ]);

        return redirect('/tweet')
          ->with('message', 'Your post has been added!');

    }

